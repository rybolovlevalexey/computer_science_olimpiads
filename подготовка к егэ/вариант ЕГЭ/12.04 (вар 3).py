ost = list()
sp = list(map(int, sp))
for i in range(n):
    ost.append(sp[i] % 43)
print(len(ost), ost.count(0), ost.count(1), sum(ost))
not_zero = list()
for i in range(len(ost)):
    if ost[i] != 0:
        not_zero.append(i)

sp_sum = list()
print(not_zero)
for i1 in [0] + not_zero[:-1]:
    for i2 in not_zero + [n]:
        if i1 > i2:
            continue
        lenght = 0
        summa = 0
        for index in range(i1, i2):
            lenght += 1
            summa += sp[index]
        sp_sum.append([summa, lenght])
print(sp_sum)

